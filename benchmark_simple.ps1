# Simple benchmark script
# Kill any previous server process
Stop-Process -Name llama-server-mini -ErrorAction SilentlyContinue

# Start the server (runs the batch file)
Start-Process -FilePath "G:\vulkan-polaris-llama\start_server.bat" -WindowStyle Normal

# Wait for server to be ready (up to 30 seconds)
$maxAttempts = 30
for ($i = 0; $i -lt $maxAttempts; $i++) {
    try {
        $null = Invoke-RestMethod -Uri "http://localhost:8080/v1/models" -Method Get -ErrorAction Stop
        Write-Host "`n✅ Server is up! Continuing..."
        break
    } catch {
        Write-Host "⏳ Attempt $($i+1)/$maxAttempts – waiting..."
        Start-Sleep -Seconds 1
    }
}
if ($i -eq $maxAttempts) {
    Write-Error "❌ Server did not become ready within 30 seconds. Abort."
    exit 1
}

# ------------------------------
# Prepare payload
# ------------------------------
$payload = @{
    model      = "qwen2.5-moe"
    messages   = @(@{ role = "user"; content = "Hello" })
    max_tokens = 32
    temperature = 0.7
    stream     = $false
}
$payloadJson = $payload | ConvertTo-Json -Depth 10

# ------------------------------
# Benchmark loop
# ------------------------------
$repeat = 5
$latencies = @()
for ($r = 1; $r -le $repeat; $r++) {
    $sw = Measure-Command {
        try {
            $resp = Invoke-RestMethod -Uri "http://localhost:8080/v1/chat/completions" `
                                      -Method Post `
                                      -Headers @{ "Content-Type" = "application/json" } `
                                      -Body $payloadJson -ErrorAction Stop
        } catch {
            Write-Warning "Request $r failed: $_"
            return
        }
    }
    $sec = $sw.TotalSeconds
    $latencies += $sw.TotalSeconds
    Write-Host "✅ Request $r done in $($sec.ToString('F3')) s"
}

# ------------------------------
# Compute stats and save results
# ------------------------------
$avgLatency = ($latencies | Measure-Object -Average).Average
$totalTokens = $repeat * 12   # assumed average output tokens
$tpm = if (($latencies | Measure-Object -Sum).Sum) { $totalTokens / ($latencies | Measure-Object -Sum).Sum } else { 0 }
$tpm = [math]::Round($tpm, 2)

# Save raw latencies
$latencies | ConvertTo-Json -Depth 5 | Set-Content -Path "docs\benchmark_latencies.json" -Encoding utf8

# Save markdown summary
$summary = @'
## Benchmark Summary (generated $(Get-Date -Format u))

- **Requests executed:** $repeat  
- **Average latency:** $($avgLatency.ToString('F3')) s  
- **Estimated output tokens per request:** 12  
- **Total tokens generated:** $totalTokens  
- **Throughput:** $tpm tokens/s (tpm)  
- **Observed VRAM usage:** ~1.62 GB of 4 GB (AMD Radeon RX 570)

Raw latency values (seconds) are in `docs\benchmark_latencies.json`.

---  
*Generated by `benchmark_simple.ps1`*  
'@
$summary | Set-Content -Path "docs\benchmark_summary.md" -Encoding utf8
Write-Host "📝 Summary written to docs\benchmark_summary.md"