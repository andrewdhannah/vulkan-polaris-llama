# ---------------------------------------------------------
# Benchmark script for the locally-running llama server
# ---------------------------------------------------------

# 1️⃣ Kill any stray server process
Stop-Process -Name llama-server-mini -ErrorAction SilentlyContinue

# 2️⃣ Start the server (background)
Start-Process -FilePath "G:\vulkan-polaris-llama\start_server.bat" -WindowStyle Normal

# 3️⃣ Wait until the HTTP endpoint is reachable (max 30 s)
$maxAttempts = 30
for ($i = 0; $i -lt $maxAttempts; $i++) {
    try {
        $null = Invoke-RestMethod -Uri "http://localhost:8080/v1/models" -Method Get -ErrorAction Stop
        Write-Host "`n✅ Server is up! Proceeding..."
        break
    } catch {
        Write-Host "⏳ Attempt $($i+1)/$maxAttempts – still waiting..."
        Start-Sleep -Seconds 1
    }
}
if ($i -eq $maxAttempts) {
    Write-Error "❌ Server did not become ready within 30 seconds. Abort."
    exit 1
}

# 4️⃣ Prepare the test payload (same payload used earlier)
$payload = @{
    model      = "qwen2.5-moe"
    messages   = @(@{ role = "user"; content = "Hello" })
    max_tokens = 32
    temperature = 0.7
    stream     = $false
}
$payloadJson = $payload | ConvertTo-Json -Depth 10

# 5️⃣ Benchmark parameters
$repeat          = 5                # number of requests
$avgOutputTokens = 12               # observed average output length for this prompt
$latencies       = @()

# ---------------------------------------------------------
# 6️⃣ Run the benchmark loop
# ---------------------------------------------------------
Write-Host "`n=== Benchmark: $repeat requests ==="
for ($r = 1; $r -le $repeat; $r++) {
    $sw = Measure-Command {
        try {
            $resp = Invoke-RestMethod -Uri "http://localhost:8080/v1/chat/completions" `
                                      -Method Post `
                                      -Headers @{ "Content-Type" = "application/json" } `
                                      -Body $payloadJson -ErrorAction Stop
        } catch {
            Write-Warning "Request $r failed: $_"
            return
        }
    }
    $elapsedSec = $sw.TotalSeconds
    $latencies += $sw.TotalSeconds
    Write-Host "✅ Request $r completed in $($sw.TotalSeconds.ToString('F3')) s"
}

# ---------------------------------------------------------
# 7️⃣ Compute statistics
# ---------------------------------------------------------
$averageLatency = ($latencies | Measure-Object -Average).Average
$totalTokens   = $repeat * $avgOutputTokens
$throughputTps = if (($latencies | Measure-Object -Sum).Sum) { $totalTokens / ($latencies | Measure-Object -Sum).Sum } else { 0 }
$throughputTps = [math]::Round($throughputTps, 2)

# ---------------------------------------------------------
# 8️⃣ Persist results for later documentation
# ---------------------------------------------------------
# 7a️⃣ Save raw latency numbers (JSON array)
$latencies | ConvertTo-Json -Depth 5 | Set-Content -Path "docs\benchmark_latencies.json" -Encoding utf8
Write-Host "📄 Latencies saved to docs\benchmark_latencies.json"

# 7b️⃣ Save a human‑readable markdown summary
$summary = @'
## Benchmark Summary (generated $(Get-Date -Format u))

- **Requests executed:** $repeat  
- **Average latency:** $($averageLatency.ToString('F3')) s  
- **Estimated output tokens per request:** $avgOutputTokens  
- **Total tokens generated:** $totalTokens  
- **Throughput:** $throughputTps tokens/s (tpm)  
- **Observed VRAM usage:** ~1.62 GB of 4 GB (AMD Radeon RX 570)

The raw latency values (seconds) are stored in `docs\benchmark_latencies.json`.

---  
*Generated by `run_benchmark_fixed.ps1`*  
'@

$summary | Set-Content -Path "docs\benchmark_summary.md" -Encoding utf8
Write-Host "📝 Summary written to docs\benchmark_summary.md"